#include "../ft_printf.h"

void	ft_puthex_maj_fd(unsigned long n, int fd)
{
	if (n >= 16)
		ft_puthex_maj_fd(n / 16, fd);
	ft_putchar_fd("0123456789ABCDEF"[n % 16], fd);
}
